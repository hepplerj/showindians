			<h2>Letter from William F. Cody to Charles H. Penney</h2>
			
			<p>North Platte, Neb. March 17th, 1894</p>

			<p>My Dear Capt. Penney</p>

			<p>I am having 500 of the contracts printed. I am going to try and come up and have a talk with You about the first of April. I will not take the Indians till about the 27th of April.  Many thanks for letting Shangrau have permission to select the Indians as it will give the Indians time to fix up their own affairs before leaving home&#8212; of course this is not final.  As I will fix all bonds &amp; contracts with you before taking them.</p>

			<p>In regard to Mr Wm. Brown who wishes to go as seasonal interpreter, I think I have promised John Shangrau to take his Brother.  But if its Tour wish what Mr Brown should go please tell Shangrau and I think it will be all right with the Shangraus.</p>

			<p>I will be in Omaha for the next five days at the Merchants Hotel&#8212;  Can I do anything for you there&#8212;</p>

			<p>
			Very kind wishes <br/>
			Yours truly
			</p>
			
			<p>
			W. F. Cody
			</p>
			
