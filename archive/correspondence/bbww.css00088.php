			<h2>Letter from William F. Cody to Mike Russell</h2>
			
			<div id="dateline"><p>Dec&#47; 27th [18]99</p></div>

			<p>North Platte Neb</p>

			<p>My Dear Mike</p>

			<p>How are we going to thank Mrs. R. and your self for your lovely and valuable Christmas presents?  Well there will be annother Christmas and Mrs. C. says we must not forget we certainly appreciate your kindness And wish you both many happy returns of the day.</p>

			<p>Mike I have been on the jump ever since I closed my show season &#8212; this is my excuse for not writeing you &#8212; I have just returned from New York. Washington Buffalo Philadelphia &amp; Chicago &#8212; And may have to go right back to Washington as there has been annother crank appointed Commissioner <!-- Page 2 -->of Indian affairs. And he says I cant have any Indians for my show. This same man came near bringing on an Indian War last fall by taking the beef hides away from the Indians. Now he wants to keep the Indians prisoners on their reservations. And not allow them to earn an honest dollar  the Indians are becoming restless cooped up on their reservations and if they are not allowed some liberty they will sooner or later give our frontier people trouble &#8212; And our frontier is no longer protected by our soldiers.  As soon as I am sure of all my attractions for my show I will go back to the Basin. everything is booming at Cody. The Mormon church wants to buy us out the Ranch and all our land &#8212; everything but the <!-- Page 3 -->townsite of Cody &#8212; that we wish to keep &#8212; I have been doing a lot of assessment work on my prospect holes &amp; placer fields &#8212; coal mines &amp;c &#8212; We will surely have our rail road if not two into Cody next summer.  My cattle &amp; horses are doing well out there. I am breaking a lot of gildens and mares this winter.</p>

			<p>Mike we all heard of your hold up and your nerve. You would have a reception at Cody as a hero.  We all drank to your health when we got the news. Remington is going to make a sketch of it.</p>

			<p>We all send our love. to you And yours &#8212;</p>

			<p>Your old friend</p>

			<p>W. F. Cody</p>

			<p>Merchants Hotel. Omaha &#8212; Jan 7th &#8212;</p>
			
