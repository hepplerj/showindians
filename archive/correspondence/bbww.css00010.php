			<h2>Letter from William F. Cody to John R. Brennan</h2>

				<p>
				Ferrill, Texas<br/>
				Oct 28th, 1907
				</p>

				<p>
				Major John R. Brennan.<br/>
				Agent Pine Ridge Agency
				So. Dak.
				</p>

				<p>My Dear Major,</p>

				<p>The Indians with me want a favor, and as they have been such good men I hope you will grant it.</p>

				<p>They have been saving and bought a great many useful things to take home.  But they are a shy bunch and it breaks an Indians heart to go home without a trunk.</p>

				<p>They would like to have $10 each the $25 a month men.  The Chiefs $15 each.  This to be taken favor their last month and back pay.  That is to be paid them in your presence.</p>

				<p>Hope you will grant their request.</p>

				<div id="closing">
				<p>Very faithfully yours</p>
				<p>W. F. Cody</p>
				</div>
				
