			<h2>Letter from William F. Cody to John R. Brennan</h2>

            <p>
            Exmouth, Eng.<br/>
			May 27th 1904
            </p>

            <p>
			John R. Brennan &#8212; Agent<br/>
			Pine Ridge Agency<br/>
			S. Dak.
            </p>

			<p>Dear Major,</p>

			<p>I am sending two Indians home who have lung trouble. And the only care for return is dry air which they can't have here. Sitting Bull Jr. and Jonas Spider the rest are all well and happy.  I will take good care of them.  I enclose my route.  Will be glad to hear how your silled with the Rail Road&#8212; In regard to those who were killed and injured.</p>

			<p>Very truly yours,<br/>
			W. F. Cody</p>
			
