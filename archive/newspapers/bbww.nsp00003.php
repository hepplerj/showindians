		<h1>BUFFALO BILL ON THE INDIAN.</h1>

		<h3>COL. CODY TELLS HOW TO SOLVE THE INDIAN QUESTION.</h3>

		<p>New York World</p>

		<p><span title="NA: Character" style="background-color: transparent;">There are two occupations which the North American Indian has abilities for, and which he will follow willingly and with energy&mdash;war and the hunt</span>.  It has been a mystery to me why our Government does not enlist the Indian in our regular army.  It would give them congenial employment, teach them discipline and make them self&ndash;sustaining.  They make the best soldiers in the world, and <span title="NA: Value" style="background-color: transparent;">we must not forget that we owe the Indians something</span>.  Again, if the Indian is enlisted as a soldier and employed in guarding our frontier, he cannot at the same time be stealing horses or carrying on a depredatory war.</p>

		<p><span title="Progressivism: Indian Welfare" style="background-color: transparent;"><span title="Image: Civilization" style="background-color: transparent;">In place of teaching them all to be farmers it would be better to make stockmen of them.  Raising cattle, horses and sheep is more to their tastes and develops their instincts naturally in a useful channel.  Ranching is but the civilization of the chase.  Teach them to raise cattle to sell</span></span>.</p>

		<p><span title="Progressivism: Indian Welfare" style="background-color: transparent;">Their schools of instruction should be upon their reservations and not 500 miles away.  Adjoining each school should be a large farm, where they could be practically taught farming and stock raising, and such trades as are useful in their own country</span>.  If you bring an Indian boy East, keep him in school for five years, teach him the tailor's trade or to be a printer, bookkeeper, musician, mechanical dentist, of what possible use is his knowledge and occupation when he returns to his home in the wild West.  He can make no practical use of it.  Again, during the five years he has been away from home his parents have had no chance to see the improvements he has been making, and he goes back to them a stranger. He feels disconnected, is not satisfied with the manner in which his parents live, and cannot live in the way in which he has been taught to in his school.  Whereas, <span title="Image: Civilization" style="background-color: transparent;"><span title="Progressivism: Indian Welfare" style="background-color: transparent;">he had been taught all this near his home, where his parents could see him growing in civilization and note his advancement, it should make them proud of their child, stimulate them to improve, and they would grow at least partly civilized and in sympathy with their fully civilized child</span></span>.</p>

		<p>As the game is gone the Indian has no further use for so much land to hunt over.  Each Indian should have a certain amount of land, should be given a deed for it.  Then would he know just which was his land, and would take a pride in ownership and improvement.  The Indian was nomadic from the necessity of following the game upon which he was dependent for life, not from a natural taste for change of landscape.  As it is now, he does not know which is his land, and takes no interest in improving it, as he does not know what day it may be taken from him.  Give each Indian 160 acres; arrange that families and relatives shall have their lands adjoining or in a body.  The remainder of the reservation, after the allotment of the 160 acres, sell.  Give them part of the money to build homes and buy farming implements, seed, horses, oxen, cows and sheep.  Take the remainder of the money derived from the sale of their excess of land and let the Government pay them interest on it.</p>

		<p>Of course the Government must still continue to help them a little, as it does now.  <span title="Image: Savagery" style="background-color: transparent;">They are very like children, they need looking after</span>.  If this were done it would save the Government probably half of the present outlay, and in a very few years the Indian would be self&ndash;sustaining and a good citizen.  As my sawed&ndash;off little partner, Nate Salsbury, is not at my elbow as I write, I say allow them to vote the Democratic ticket, if they want to.  They have as good a natural right to vote as anybody.  They have been Americans for some time, and I know they would appreciate the confidence thus placed in them.  Besides, it is the only way I know to get into Congress.</p>

