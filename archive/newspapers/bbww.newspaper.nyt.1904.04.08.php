		<h1>INDIANS KILLED IN COLLISION.</h1>

		<h3>Three Dead, Three Fatally Injured, and a Score Hurt.</h3>

		<p>A special car containing sixty&ndash;three Indians belonging to Buffalo Bill's Wild West Show was smashed in a collision between the Oregon Express and a fast mail train on the Chicago and Northwestern Railway near Maywood, Ill., to&ndash;day during a fog.  Both trains were east&ndash;bound.</p>

		<p>Three of the Indians were instantly killed, three were fatally injured, and twenty others more or less seriously hurt.  The Indians who were not pinned in the wreck fled in a panic across the prairie, but returned and, led by Chief Iron Tail, solemnly chanted the Indian &quot;death song&quot; as they gathered around the bodies which had been laid out beside the tracks.  Those killed were Kill Head, Phillip Iron Tail, Jr., and Thomas Come Last.</p>

		<p>Chief Whitehorse, in charge of the Indians on the train, was among those fatally injured.  He said he knew death was near and requested that he be placed near his dead companions.  The Chief was propped up and sat stoically smoking a pipe while physicians worked over his injuries pending arrangements to take the injured were Annie Good Face and Luther Standing Bear.</p>

		<p>No white passengers were hurt.</p>

		<p>The Indians were on their way to New York.</p>

			
