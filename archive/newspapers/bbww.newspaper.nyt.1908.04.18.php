		<h1>WILD WEST CHIEFS INITIATED.</h1>

		<h3>Buffalo Bill's Red Men Now Have a Friars' Order of Their Own.</h3>

		<p>Following a solemn ceremony <span title="NA: Members" style="background-color: transparent;">Chiefs Iron Tail, Lone Bear, Rocky Bear, and one or two other red men</span> were made friars.  They do not, however, aspire to the body of publicists that parades the Great White Way in New York, although when they come here they hope to be taken into the fold by the Abbot.</p>

		<p>The friar in &quot;Buffalo Bill's&quot; Wild West Indian camp means that <span title="Image: Civilization" style="background-color: transparent;">these friarly red men have been placed in charge of all the others.  Interpreters have also joined the cult, and it is said that there will be a great brotherly feeling among the Indians of the Wild West, and when the fifty&ndash;seven Japs come to the show the chiefs will try to convert them, too</span>.</p>

		<p>&quot;Johnny&quot; Baker, with one of the Indians who speaks English, was the chief friar of the occasion, and read the rules of the brotherhood, and the only reason for calling the red men by that name was because they wore their blankets like the monks wear their cowls.  They will be told to come and visit the real friars in this city at one of the Friday night gatherings if Mr. Wells is willing.</p>

		<p>&quot;No white squaw there?&quot; asked Iron Tail, evidently hoping that the party would be a stag one.  When informed that not even red women would take part he grunted with delight.</p>

		<p>After the ceremony the red men went through a fine sun dance, just to show that they believed they had been elevated above the ordinary term of &quot;chief.&quot;</p>

			
