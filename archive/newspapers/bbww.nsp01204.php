		<h1>PLENTY OF &quot;WILD WEST&quot; ENTHUSIASM.</h1>

		<h3>A Group of Howling Savages Pursue a Defenseless Stage Coach</h3>

		<p>The old Deadwood stage, drawn by six spirited mules, drew up in front of the grand stand at Athletic Park last night and took on several passengers.  John Higby, a companion of the renowned Hank Monk, was on the box and, escorted by two scouts, started on his journey in true Western style.  <span title="NA: Performances" style="background-color: transparent;"><span title="NA: Description" style="background-color: transparent;">Suddenly a piercing yell was heard, and a party of mounted Indians galloped from behind a canvas sheet, where they had been lying in ambush, and pursued the coach</span></span>.  Higby lashed his mules into a furious gallop and rushed madly over the cinder track, the coach jumping and swaying from side to side.  <span title="NA: Performances" style="background-color: transparent;"><span title="Image: Savagery" style="background-color: transparent;"><span title="NA: Description" style="background-color: transparent;">Nearer and nearer came the Indians, yelling like mad</span> and exchanging rapid shots with the passengers.  As the coach turned the curve by the stand again the foremost Indians came up with it, pouring shot after shot into the driver, whose capacity for holding lead seemed unlimited</span></span>.  The spectators sat spellbound.  <span title="NA: Performances" style="background-color: transparent;">Suddenly another body of horsemen appeared, headed by &quot;Buffalo Bill&quot; and charged on the yelling savages.  A desperate encounter ensued which resulted in the flight of the Indians and the rescue of the coach, and <span title="BBWW: Genuine" style="background-color: transparent;">the thrilling act ended in a blaze of Grecian fire from the interior of the vehicle in a realistic manner peculiar to the original genius of the West</span></span>.  The other features of the &quot;Wild West&quot; show were greeted with equal enthusiasm and proved greatly entertaining.</p>

