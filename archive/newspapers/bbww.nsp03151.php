			<h2>PAINTED HORSE</h2>

			<h4>He Charges Dr. Carver and Buffalo Bill With Great Cruelty.</h4>

			<p>Painted Horse, one of the Indians from the Red Cloud agency who was with Dr. Carver's show in Europe, arrived here Saturday on the steamer Augusta Victoria.  <span title="NA: Treatment" style="background-color: transparent;">Today in the presence of General O'Bierne he made a long statement in which he charged both Carver and Buffalo Bill with great cruelty toward the Indians under their charge.  Painted Horse said he was repeatedly tied up until he could endure no further suffering.  He was fed upon &quot;stinking meat&quot; and other things wholly unfit for food; his money was taken from him and when he asked its return he received only $5.  He said other Indians were also shamefully treated and were repeatedly fired upon with both blank and ball cartridges and badly wounded</span>.  <span title="BBWW: Immoral" style="background-color: transparent;">They were permitted to have all the whiskey they wanted and when under its influence they often fought among themselves</span>.  Gen. O'Bierne has a copy of the agreement entered into by the Carver combination in which good treatment to the Indians is specified particularly as one of the conditions of their engagement.  A copy of Painted Horse's statement will be prepared and forwarded to the secretary of the Interior and to the Indian commissioners.  According to the latest advices Buffalo Bill's Indians will arrive in Philadelphia next Thursday.</p>

			
