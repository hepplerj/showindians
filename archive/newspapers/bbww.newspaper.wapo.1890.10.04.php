		<h1>Indians Cannot Join &quot;Wild West&quot; Shows.</h1>

		<p>Acting Indian Commissioner Belt has issued a letter of instructions to Indian agents, in which he says that the Department is informed that a company is preparing to obtain Indians from some of the reservations to join &quot;Wild West shows&quot; in Europe.  The agents are instructed to promptly refuse any applications for this purpose, as it is now against the policy of the Interior Department to grant permits for such purposes under any circumstances whatever.  The agents are further instructed to lay the matter before their respective tribes and advice them that if any of the Indians should hereafter attempt to leave their reservation for exhibition purposes, it will be regarded as an open defiance of the authority of the Government, and that prompt measures will be adopted to detain them.</p>

