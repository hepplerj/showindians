		<h1>INDIANS WELL TREATED.</h1>

		<p>The thirty&ndash;six Sioux Indians who for the last three or four years have been connected with the Buffalo Bill&ndash;Salsbury Wild West show in Europe were given a hearing to&ndash;day by Acting Indian Commissioner Belt as to the truth of the statements of ill&ndash;treatment which have been made from time to time.  The Indians were privately examined by Mr. Belt as to the facts of the case.</p>

		<p>At the conclusion of this private hearing the doors were opened.  <span title="NA: Treatment" style="background-color: transparent;">Mr. Belt said that the Indians had told him that they had no complaints to make.  Their contracts had been faithfully carried out; they were paid regularly and promptly, and they were furnished good, wholesome food in abundance.  One Indian spoke of Gen. O'Beirne, the Assistant Immigration Commissioner of New York, and said that he &quot;should not be listened to.&quot;  He had already made them much trouble</span>.</p>

			
