		<h1>WILL ACCOMPANY &quot;BUFFALO BILL.&quot;</h1>

		<h3>The Bad Indians at Fort Sheridan to Give a &quot;Wild&ndash;West&quot; Show.</h3>

		<p>The <span title="NA: Members" style="background-color: transparent;">twenty&ndash;six Ogallala and Brule braves of the Sioux tribe and the three squaws</span> now at Fort Sheridan <span title="Context: Indian Wars" style="background-color: transparent;">will not return to their reservations to stir up another bloody war, as has been predicted</span>.  <span title="NA: Travel" style="background-color: transparent;"><span title="Image: Civilization" style="background-color: transparent;">They will go abroad to hobnob with the nobility of Europe</span></span>.  This unexpected change of affairs was brought about by Col. W. F. Cody, better known as &quot;Buffalo Bill.&quot;  The Colonel desiring to secure more red men for his European tour this summer, first obtained the consent of Secretary Blaine and the Department of the Interior and then went to Fort Sheridan to learn how the Indians felt about the matter.  This visit was made Thursday afternoon.  He had a long talk with <span title="NA: Members" style="background-color: transparent;">Kicking Bear, Revenge, One Bull, Killed Close to Home, One Star, Wounded with Many Arrows, Brave, Standing Bear, Noah's Horses Voice, Short Bull, and the rest of the tribe</span>.  The Indians did not need much persuasion, and readily accepted &quot;Buffalo Bill's&quot; offer to accompany him across the &quot;great river.&quot;</p>

		<p>&quot;The date of our departure has not been decided,&quot; said Col. Cody last evening, &quot;because <span title="NA: Character" style="background-color: transparent;">an Indian has to be given several days in which to change his mind three or four times</span>. I visited Fort Sheridan again this afternoon, but found some of the braves in an entirely different mood than the day before.  Half of the number who had consented yesterday to go to Europe with me refused to talk further about the proposition. They will go, however. <span title="NA: Character" style="background-color: transparent;">They always change their minds several times before finally deciding what to do.  Maj. Burke has the thing in charge now and is able to make an Indian do just as he wants him to</span>.&quot;</p>

			
