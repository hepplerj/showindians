		<h1>INDIANS ON THE EAST TRACK</h1>

		<h3>They Are Not Delighted With Their New Camp&#8212;Two Great Concerts.</h3>

		<p>The Indians are now located on the east tract, south of the Horticulture building.  The move was made yesterday, the tepees all being put up and teams busy transporting the fuel supply and commissars department, the latter being located in the Georgia building.  The horses are still in the Transportation building, where they will stay.  The Indians are not at all pleased with the change, and were in ill humor yesterday.  Many of them expressed a desire to go home.  They do not like being separated from their horses, having to walk back and forth to attend to them.</p>

		<p>Last night three hungry Cheyennes came in from Chicago and are the guests of the Sioux.  They were given a fine supper and took in the Midway last night.  They are <span title="BBWW: Travel" style="background-color: transparent;">members of Buffalo Bill's Wild West show now at Chicago and they are en route home for a short rest.  They will join the show when it comes to Omaha</span>.  Several of the Sioux at the exposition have been with the Buffalo Bill show, and the three visitors are their guests.</p>

		<p><span title="NA: Descriptions" style="background-color: transparent;">There is more mourning in the Indian camp on account of news from the Rushville reservation that the little child of Chief White Face is very sick and not expected to live.  The father left for his home in the northwest last night.  He will not return</span>.</p>

		<p>President Miller left yesterday for Dakota City, where he is to be the orator of the day at the annual picnic of the old settlers' reunion.  He will return tomorrow.</p>

		<p>Saturday, September 2, is down on the boards for Douglas county day, which will be observed in the Agriculture building.  The band will be stationed on the hurricane deck of the battleship Maine in the building, and some speeches will be made and an effort made to observe in a fitting way the completion of the Douglas county exhibit.</p>

		<p><span title="NA: Performances" style="background-color: transparent;">The program for today calls for races, in which the Indians and others will take part</span>.  There will be fireworks this evening for the benefit of the First Nebraska boys should they arrive.</p>

		<p>Miss Susie Laborg of Madison, S.D. will spend several days at the exposition, the guest of the Sentinel of that city, the young lady winning the trip at the expense of the newspaper as the most popular young woman in the city, receiving 7302 votes.  She comes in this week.</p>

		<p>Captain Frank Sosey and wife of Palmyra, Mo. will be in the city on Thursday, coming with Misses Frankie Connell and Cora Crim, two teachers of Marion county, who are first and second in the popular teachers' contest of that paper closed there a few days ago.  The Palmyra Spectator is probably the oldest paper in Missouri having been in existence sixty years and six months and having been in the Sosey family all those years.  It was founded by Jacob Sosey, deceased, who was its editor until ten years ago, since which time his two sons have conducted the enterprise.</p>

		<p>The concert program was an excellent one, each selection pleasing greatly.  The audience last night was a very intelligent one, being composed of Omaha's best called out to hear and enjoy genuine good music.  The program was arranged for the Auditorium, at which place the University quartet was expected to take part in a grand concert, but the singers were unable to come and the same program was given at the Plaza.  The program for this evening is made up as a special for the Fighting First, being largely composed of patriotic airs, with a repetition of &quot;Dewey at Manila,&quot; which will be given in a brilliant manner.</p>

