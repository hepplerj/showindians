		<h1>WILL HAVE NO &quot;WILD WEST&quot; SHOW.</h1>

		<h3>Exposition Officials Do Not Approve of One in the Indian Exhibit.</h3>

		<p>Chief Putnam of the Department of Ethnology and Commissioner Morgan of the Indian Bureau at Washington have decided to <span title="BBWW: Immoral" style="background-color: transparent;">lend no countenance to a &quot;Wild West&quot; show business in connection with the Indian exhibit at the World's Fair</span>.  Prof. Putnam said yesterday that the prime object of &quot;Wild West&quot; exhibitions is to <span title="BBWW: Immoral" style="background-color: transparent;"><span title="Image: Savagery" style="background-color: transparent;">make money by a display of the savage and repulsive features of Indian life</span></span>.  None of this, he said, will appear in the Indian exhibit.</p>

		<p>&quot;The great purpose of the exhibit of educated Indians,&quot; said Prof. Putnam, &quot;in connection with the uneducated engaged in their native industries is to <span title="Image: Civilization" style="background-color: transparent;"><span title="Progressivism: Reform" style="background-color: transparent;">show conclusively that the effort of the government to educate and civilize these people is being made a triumphant success</span></span>.  Accordingly the Indian Bureau will have the model industrial school in full operation.  The building, although cheap, will be attractive.  The windows are to be filled with transparencies representing scenes in the Indian country.  On the floors will be rugs and mats of Indian manufacture, and the walls will be decorated with all sorts of Indian work, including baskets, pottery, blankets, etc.</p>

		<p>&quot;It is expected that there will be successive delegations of pupils, staying from one to three weeks at a time, from the various classes of Indian schools now in operation.  These Indians will live in their tepees, dress in their native costumes, and so far as possible carry on their native pursuits.&quot;</p>

			
