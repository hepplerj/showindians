		<h1>BUFFALO BILL'S SHOW DRAWS</h1>

		<h3>Best Attendance Ever Attracted to a Performance in Omaha.</h3>

		<p><span title="BBWW: Attendance" style="background-color: transparent;">It seems as if the world will never tire of Buffalo Bill's Wild West show, and it seemed as if all the world was out there yesterday, when the attendance broke the record, for the performance in Omaha</span>.  While the prodigious swarms of people which usually crowd the thoroughfares on the mornings of circus processions were noticably absent yesterday, there was no such feature conspicuous in the attendance at the grounds in the afternoon.</p>

		<p>Long before the ticket wagon threw up its windows for the commencement of business the spacious area way surrounding the big canvas structure was fairly working with eager hordes.  They crowded and jostled everywhere, about the hustling ticket sellers, the broad entrance, around the main pavilion and side shows and into the adjacent streets.  When the doors were once thrown open, it did not require any very extended time to pack the huge structure to its capacity.  <span title="BBWW: Attendance" style="background-color: transparent;">Every inch of seating space was occupied, and all about the arena, between the seats and the separating ropes, was a sea of people, and hundreds could obtain accommodations of no kind.  All about the grounds, the trees, telephone poles and housetops were specked with men and boys and one would have thought the wonderous mines of Jarubid were about to be opened to the eyes of an insane populace.  The paid admissions to the afternoon performance exceeded 14,000 which means that 20,000 pair of eyes witnessed the spectacle</span>. A peculiar thing about this great crowd was the fact that the bulk of it was undoubtedly from the rural districts.  Ordinarily the people of the city predominate at about a rate of ten to one on such occasions, but yesterday it was the sturdy countrymen and their families who were in the majority.</p>

		<p><span title="BBWW: Genuine" style="background-color: transparent;">There is but little of the old&ndash;time circus glamor surrounding Colonel Cody's show.  It is distinctly original, such as man never essayed before, and which man cannot possibly consummate again for lack of material</span>.  <span title="NA: Descriptions" style="background-color: transparent;">Such another assemblage of North American Indians cannot be brought together and it is boldly asserted here that the Indians are the biggest drawing power, aside from the immortal name of the old buffalo killer himself that the Wild West possesses.  Apropos, the three mighty bursts of thunderous applause which shook earth and atmosphere yesterday afternoon during the performance were occasioned, first, by the on rails of the scarlet warriors, on the gafly caparisoned little rats of steeds, which opened that grand picture, the Congress of Nations</span>; second, the appearance of Old Glory, streaming from its staff in the hands of a United States trooper as he sped by, and third, by the debut of Colonel Cody himself.</p>

		<p>The battle of San Juan Hill is the only innovation to the list, and it is a superb one, but the Wild West requires no innovations as if is and has been it is good enough for this generation and maybe generations to come.  The crowds attest to this.  They want Buffalo Bill and Buffalo Bill's Wild West.</p>

		<p>Before the performances yesterday Colonel Cody was waited upon by the <span title="NA: Descriptions" style="background-color: transparent;">entire aggregation of Sioux Indians now on exhibition at the Greater America exhibition.  It seems that <span title="Context: Indian Wars" style="background-color: transparent;">the Sioux tribes are at present in council at Pine Ridge agency, pow&ndash;powing over one of their periodical difficulties with the government</span>, and they have come to their old friend, Colonel Cody, to intercede for them</span>.  Little Wound, the grand of the exposition aggregation, was deputized by the forces at the agency to wait upon Colonel Cody, and it was he who did the speaking yesterday.  He informed the colonel of the nature of the trouble, and also that he had been instructed to implore his assistance and friendship.  <span title="Image: Noble Savage" style="background-color: transparent;">The Indians want the good Pa&ndash;has&ndash;Ra&mdash;Buffalo Bill's Sioux cognemen, and which means the man&ndash;with&ndash;the&ndash;long&ndash;hair&mdash;to accompany them for Washington in November and plead for them with the Great White Father, and living and able to travel in November.  Colonel Cody will probably make the trip with his red worshippers and endeavor to compass their desires with the president</span>.</p>

		<p>Another little incident marked the round of the day.  After the performance, Miss Irma Cody, the colonel's daughter, and Mrs. Cody, were presented with tokens of esteem and affection by the entire company.  Miss Irma, receiving a handsome friendship heart bracelet of silver with a portion to typify each of the sixty&ndash;seven departments of the Wild West show, with a beautiful writing set.  Annie Oakley, the greatest living woman shot in the world, and a sprightly little lady withal, made the presentation speech to Mrs. Cody and J. J. McCarthy, on behalf of the givers and a few words of tribute to Miss Irma Cody, to which the ladies responded.</p>

