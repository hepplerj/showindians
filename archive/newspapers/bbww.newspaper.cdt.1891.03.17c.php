		<h1>WHAT PRESIDENT HARRISON SAID.</h1>

		<h3>He Does Not Like &quot;Wild West&quot; Shows of the Buffalo Bill Variety.</h3>

		<p>At a meeting of the Congregational Club last evening Mrs. Mary C. Collins, who had been deputed by the club to go to Washington and endeavor to prevent Buffalo Bill from taking Short Bear and the other Indians with him, made her report, in which she said:</p>

		<p>&quot;March 19, in company with Col. Whittlesey, a Secretary of the Board of Indian Commissioners, I interviewed President Harrison.  <span title="BBWW: Immoral" style="background-color: transparent;">He said he had no sympathy with the Indian show business.  He said he was especially opposed to making such use of the prisoners; that it was much like rewarding the bad boy.  He then directed me to carry the resolutions to Secretary Noble and ask him to look into the matter and report to him (the President)</span>.</p>

		<p>&quot;I went to Secretary Noble.  He seemed to be much excited and said he wished it to be understood that public meetings could not drive him.</p>

		<p>&quot;'Why,' he said, 'did you not write me a private letter telling me these things, instead of going before a public meeting in Chicago, at which a public officer had been asked to speak, to give official color to it?'</p>

		<p>&quot;After some more discussion he read the letter and resolutions and listened courteously while I explained the situation.  He acknowledged that he had formerly been opposed to the Indians going with Buffalo Bill, but acted this time on the advice of Assistant Indian Commissioner Belt.  He further said that <span title="BBWW: Instructive" style="background-color: transparent;"><span title="Context: Indian Wars" style="background-color: transparent;">they would be better off earning money than at home doing nothing or fighting the Government</span></span>.&quot;</p>

		<p>Miss Collins was sent to the War Department, where Assistant Secretary Grant received her kindly, and said that if the Indians did not wish to go to Europe they need not. </p>

		<p>&quot;He said the War Department had not been consulted in regard to bringing in the prisoners,&quot; she continued, &quot;and he did not know what these Indians had done or why they were prisoners.  He said he would think it over and confer with Secretary Noble, as the Secretary of the Interior had himself suggested that those prisoners be allowed to go with the show.&quot;</p>

		<p>Miss Collins then saw Assistant Indian Commissioner Belt, who she says flatly contradicted Secretary Noble, saying he was wholly opposed to the show business.  He then showed her the book containing the questions asked the returned Indians and the answer, also his letter to Secretary Noble.</p>

		<p>Miss Collins made several attempts to accomplish something, but, as her report shows, in vain.  The rest of the report is given up to her resume of the old charges that <span title="BBWW: Immoral" style="background-color: transparent;"><span title="Progressivism: Indian Welfare" style="background-color: transparent;"><span title="Image: Immoral" style="background-color: transparent;">Buffalo Bill conducts a low show, teaches the Indians bad habits, and advertises fraudulently</span></span></span>.  At the conclusion of her remarks a resolution was adopted that they be printed.</p>

			
